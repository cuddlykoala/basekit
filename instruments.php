            <h2>Instruments in our lab</h2>
            <p>The biointerface lab is equipped with state-of-the-art
              spectroscopy techniques, sepcifically dedicated to the
              analysis of sample surfaces and interfaces. We also have a
              setup for the synthesis of nano-microparticles in highly
              controlled conditions. If you are interested in accessing
              any of these instruments, please <a href="#email" class="author-name"> contact us</a>.</p>
            <h3>Confocal Raman / fluorescence microscope</h3>
            <img src="http://biointerfacelab.mcgill.ca/images/Senterra.jpg" alt="Bruker Senterra" class="float-left" width="200">
            <p>We have a
              fantastic Bruker Senterra confocal Raman microscope
              equipped with 3 lasers (785, 633 and 532 nm), which allows
              us to analyze a vast range of samples, including
              biological samples. A special hardware feature removes
              fluorescence background at 785 nm. Sample maps with
              approximately 1 micron resolution can be obtained with a
              computer controlled sample stage, and polarizers and
              analyzers allow us to take polarized spectra.
              Additionally, we can measure fluorescence maps using the
              Olympus optical microscope installed on the instrument.
              The connection with the <a href="multiram">MultiRAM</a>
              allows us to measure spectra with excitation wavelength of
              1064 nm too. <br>
              <br>
              <strong><a href="#top" class="back-to-top">Back to Top</a><br>
              </strong></p>
            <h3>NIR FT-Raman spectrometer</h3>
            <img src="http://biointerfacelab.mcgill.ca/images/multiram.jpg" alt="Bruker Multiram" class="float-left" width="200"> 
            <p>The Bruker MultiRAM
              spectrometer is a stand-alone FT-Raman spectrometer with
              excitation wavelength of 1064 nm. We have connected this
              instrument to the Senterra microscope via a fiber optic,
              thus achieving the rare possibility of collecting FT-Raman
              spectra inside a confocal microscope. <br>
              <br>
              <strong><a href="#top" class="back-to-top">Back to Top</a><br>
                <br>
              </strong></p>
            <h3>Microcalorimetry of adsorption</h3>
            <img src="http://biointerfacelab.mcgill.ca/images/tamIII.jpg" alt="TAM III" class="float-left" width="200"> 
            <p>The microcalorimeter TAM
              III from TA instruments is an amazingly sensitive
              microcalorimeter
              of adsorption, which we can use to measure energies of
              interaction between samples in liquid, solid or gas phase.
              We have two cells, one to perform isotermal titration
              calorimetry measures, and another that allows us to
              measure the heat of interaction between two samples simply
              by putting them in contact with each other.<br>
              <br>
              <strong><a href="#top" class="back-to-top">Back to Top</a><br>
                <br>
              </strong></p>
            <h3>IR spectrometer</h3>
            <img src="http://biointerfacelab.mcgill.ca/images/Tensor.jpg" alt="Bruker Tensor" class="float-left" width="200"> 
            <p>We have a Bruker Tensor
              27 IR spectrometer, equipped with both a DTGS and an MCT
              detector. Several attachments allow us to perform measures
              specifically at interfaces: multi-pass attenuated total
              reflectance (ATR), with which we can take spectra of
              samples in water; grazing-angle ATR (GATR), the best
              technique to analyze self-assembled monolayers; a diffuse
              reflectance (DRIFT) cell, which we can heat and connect to
              a gas delivery line, to analyze changes in surface
              functional groups. <br>
              <br>
              <strong><a href="#top" class="back-to-top">Back to Top</a><br>
                <br>
              </strong></p>
            <h3>Titrando</h3>
            <img src="http://biointerfacelab.mcgill.ca/images/Titrando.jpg" alt="Titrando" class="float-left" width="100">
            <p>A pH-Stat is
              usually used in analytical chemistry applications to
              titrate acid/base solutions, or metals. In the
              biointerface lab we use Titrando 902 (Metrohm, Fischer) to
              perform crystallization experiments in very controlled
              conditions. We have both a pH and a Ca sensitive
              electrode, which allow us to work in constant composition
              regimes, to mimic body conditions. A water-jacketed cell
              connected to a water bath allows us to keep the
              temperature constant as well.&nbsp;</p>
            
