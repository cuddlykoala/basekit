              University. Her BS project was about superplasticity of
              ceramics. She then left for Singapore to pursue a Master
              in the field of Mechanical Engineering-Manufacturing at
              Nanyang Technological University. For her Master’s
              project, she worked on the crystallization and growth
              kinetics of nano CeO2 powder prepared via homogeneous
              precipitation and also studied the sintering and
              mechanical properties of CeO2-based nano composite. In
              2008 she finished her Master’s in Singapore, and went back
              to her home country. In winter 2010 she joined Mcgill for
              her PhD, and currently she is working with Prof Cerruti in
              the field of biomaterials engineering. For her PhD
              project, she is working on the synthesis of hydroxyapatite
              (HA), which is the main inorganic component of bone. Her
              goal is to determine the inhibitory or promoting effect of
              a variety of biomolecules like amino acids (AAs) and
              peptides on HA crystallization at physiological
              conditions. For this purpose, she is using different
              characterization techniques, such as FT-IR, ICP, XRD, XPS,
              TEM, and SEM. She is also planning on modeling HA
              crystallization based on kinetical and thermodynamical
              results, in order to determine the growth mechanism of HA
              in the presence of AAs. </p></div>
